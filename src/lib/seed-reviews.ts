export async function seedTeacherJonReviews(): Promise<number> {
  // Get teacher profile to get the ID
  const profile = await getTeacherProfileByUsername('teacherjon');
  
  if (!profile) {
    throw new Error('Teacher profile not found. Please seed the profile first.');
  }

  const teacherId = profile.id;
  
  // Check if reviews already exist
  const existingReviews = await getReviewsByTeacher(teacherId, false);
  if (existingReviews.length > 0) {
    console.log(`Found ${existingReviews.length} existing reviews. Skipping seed.`);
    return existingReviews.length;
  }

  // Create all reviews
  let count = 0;
  for (const review of italkiReviews) {
    await createReview({
      ...review,
      teacherId,
    });
    count++;
  }

  console.log(`Seeded ${count} reviews!`);
  return count;
}
